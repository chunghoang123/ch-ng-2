#include<stdio.h>
int main(){
	// khai bao kieu du lieu so nguyen
	int soNguyen = 10;
	printf("so nguyen la %d\n",soNguyen);
	// khai bao kieu du lieu so thuc
	float soThuc = 3.14;
	printf("so thuc la %.2f\n",soThuc);
	
	// khai bao kieu du lieu so thuc chinh xac
	double soThucChinhXac = 3.141592653589793;
	printf("so thuc chinh xac la %f\n",soThucChinhXac);
	
	// khai bao kieu du lieu ky tu 
	char kyTu = 'a';
	printf("ky tu la %c\n",kyTu);
	
	return 0;
}
